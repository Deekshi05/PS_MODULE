export default function Dropdown({ value, options, onChange }) {
  return (
    <div className="dashboardDropdown">
      <select value={value} onChange={(event) => onChange?.(event.target.value)}>
        {options.map((option) => (
          <option key={option.value} value={option.value}>
            {option.label}
          </option>
        ))}
      </select>
    </div>
  );
}
